def bank_suggestion_hint(source: BankEventSuggestionSource) -> str | None:
    match source:
        case BankEventSuggestionSource.PARSER_HINT:
            return (
                "Почему так: SMS или продавец похожи на известные слова этой категории. "
                "Если не подходит, выберите другую категорию."
            )
        case BankEventSuggestionSource.LEARNED_RULE:
            return (
                "Почему так: похожего продавца уже подтверждали в этой категории. "
                "Если сейчас это не так, измените категорию или отключите правило."
            )
        case BankEventSuggestionSource.MANUAL:
            return "Почему так: категорию выбрали вручную."
        case BankEventSuggestionSource.NONE:
            return None
